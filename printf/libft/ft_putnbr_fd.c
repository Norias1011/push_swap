#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	long	c;

	if (fd == 0)
		return ;
	c = n;
	if (c < 0)
	{
		write(fd, "-", 1);
		c = c * -1;
	}
	if (c >= 10)
	{
		ft_putnbr_fd(c / 10, fd);
		c = c % 10;
	}
	if (c < 10)
	{
		c = c + '0';
		write(fd, &c, 1);
	}
}
